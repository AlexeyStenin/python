import random  # random.randint(A, B)- случайное целое число N, A ≤ N ≤ B - pythonword.ru

n = int(input('Введите целое число: '))
rand_list = []
x = 0
while x <= n:
    rand_list.insert(x, random.randint(-100, 100))
    x += 1

