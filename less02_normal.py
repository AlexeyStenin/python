# Task1

import math

numbers = [-1, 0, 256, 10, 25, 49]
root_numbers = []
for i in numbers:
    if i > 0 and (math.sqrt(i) - int(math.sqrt(i))) == 0:
        root_numbers.insert(i, int(math.sqrt(i)))
print(root_numbers)

